#!/usr/bin/env python3
"""
Stage 3 — Judge LLM.

Reads evals/data/rag_outputs.jsonl, compares each RAG answer against the
reference answer using a Judge LLM, and writes evals/data/judge_results.jsonl.

The judge scores the RAG answer on correctness and completeness (1–5) and
explains what key facts are missing. Judge calls run in parallel.

Results are broken down by sector, question_type, and filing year so you can
pinpoint where the RAG system underperforms.

Usage:
    python -m evals.run_judge
    python -m evals.run_judge --judge-model gpt-5.4-mini
    python -m evals.run_judge --workers 8
    python -m evals.run_judge --input evals/data/rag_outputs.jsonl
"""

from __future__ import annotations

import argparse
import json
import re
import sys
import time
from collections import defaultdict
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

DATA_DIR    = Path(__file__).parent / "data"
RESULTS_DIR = Path(__file__).parent / "results"

JUDGE_SYSTEM = """\
You are an expert evaluator for a financial research AI system that answers
questions about SEC filings (10-K annual reports and 10-Q quarterly reports).

You will be given:
  - A question asked by a user
  - A reference answer written by an expert who read the source documents
  - A candidate answer generated by the AI system

Score the candidate answer 1–5:
  5 — Fully correct and complete; all key facts from the reference are present;
      no significant claims contradict the reference
  4 — Mostly correct; covers the main points with minor gaps or omissions
  3 — Partially correct; gets some facts right but misses important content,
      or includes one clearly incorrect claim
  2 — Mostly incorrect or too vague to be useful; surface framing may be right
      but specifics are wrong or absent
  1 — Factually wrong, completely off-topic, or refused to answer

Important:
  - A candidate that paraphrases the reference correctly should score 4 or 5.
    Do NOT penalise different wording that conveys the same facts.
  - A candidate with extra correct detail beyond the reference is fine.
  - A candidate that is vague or generic without specific facts scores 2–3.

Return ONLY valid JSON — no markdown, no explanation:
{{"score": <int 1-5>, "reasoning": "<2-3 sentences>", "missing": "<key facts absent from candidate answer, or 'none'>"}}"""

JUDGE_USER = """\
Question: {question}

Reference answer:
{reference_answer}

Candidate answer:
{rag_answer}"""


# ── Judge call ────────────────────────────────────────────────────────────────

def _judge_one(args: tuple) -> tuple[int, dict]:
    """Thread worker: call the Judge LLM for one item."""
    idx, item, llm, max_retries = args
    user = JUDGE_USER.format(
        question=item["question"],
        reference_answer=item.get("reference_answer", ""),
        rag_answer=item.get("rag_answer", ""),
    )

    for attempt in range(max_retries + 1):
        try:
            raw = llm.complete(
                system=JUDGE_SYSTEM,
                user=user,
                temperature=0,
                max_tokens=256,
            )
            raw = raw.strip()
            if raw.startswith("```"):
                raw = re.sub(r"^```[a-z]*\n?", "", raw)
                raw = re.sub(r"\n?```$", "", raw)
            result = json.loads(raw)
            score  = max(1, min(5, int(result["score"])))
            return idx, {
                "id":               item["id"],
                "question":         item["question"],
                "sector":           item.get("sector", ""),
                "question_type":    item.get("question_type", ""),
                "source_files":     item.get("source_files", []),
                "reference_answer": item.get("reference_answer", ""),
                "rag_answer":       item.get("rag_answer", ""),
                "rag_model":        item.get("rag_model", ""),
                "score":            score,
                "reasoning":        result.get("reasoning", ""),
                "missing":          result.get("missing", ""),
            }
        except (json.JSONDecodeError, KeyError, ValueError) as e:
            if attempt < max_retries:
                time.sleep(1)
                continue
            print(f"  Warning [{item.get('id')}] judge parse error: {e}",
                  file=sys.stderr, flush=True)
        except Exception as e:
            print(f"  Warning [{item.get('id')}] judge call failed: {e}",
                  file=sys.stderr, flush=True)
            break

    return idx, {
        "id": item.get("id", ""), "score": 0,
        "reasoning": "Judge call failed", "missing": "",
        "question": item.get("question", ""),
        "sector": item.get("sector", ""),
        "question_type": item.get("question_type", ""),
        "source_files": item.get("source_files", []),
        "reference_answer": item.get("reference_answer", ""),
        "rag_answer": item.get("rag_answer", ""),
        "rag_model": item.get("rag_model", ""),
    }


# ── Summary ───────────────────────────────────────────────────────────────────

def _avg(vals: list[float]) -> float:
    return round(sum(vals) / len(vals), 3) if vals else 0.0


def build_summary(results: list[dict]) -> dict:
    scores = [r["score"] for r in results if r["score"] > 0]
    summary = {
        "n":             len(results),
        "mean_score":    _avg(scores),
        "pct_gte_4":     round(100 * sum(s >= 4 for s in scores) / len(scores), 1) if scores else 0,
        "pct_lte_2":     round(100 * sum(s <= 2 for s in scores) / len(scores), 1) if scores else 0,
        "score_dist":    {str(i): scores.count(i) for i in range(1, 6)},
    }

    # Break down by sector
    by_sector: dict[str, list[int]] = defaultdict(list)
    for r in results:
        if r["score"] > 0:
            by_sector[r.get("sector", "unknown")].append(r["score"])
    summary["by_sector"] = {
        s: {"mean": _avg(v), "n": len(v)} for s, v in sorted(by_sector.items())
    }

    # Break down by question type
    by_type: dict[str, list[int]] = defaultdict(list)
    for r in results:
        if r["score"] > 0:
            by_type[r.get("question_type", "unknown")].append(r["score"])
    summary["by_question_type"] = {
        t: {"mean": _avg(v), "n": len(v)} for t, v in sorted(by_type.items())
    }

    # Break down by year (extracted from source_files)
    by_year: dict[str, list[int]] = defaultdict(list)
    for r in results:
        if r["score"] > 0:
            for sf in r.get("source_files", []):
                m = re.search(r"(\d{4})-\d{2}-\d{2}", sf)
                if m:
                    by_year[m.group(1)].append(r["score"])
                    break
    summary["by_year"] = {
        y: {"mean": _avg(v), "n": len(v)} for y, v in sorted(by_year.items())
    }

    return summary


def write_summary_md(summary: dict, results: list[dict], timestamp: str) -> Path:
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    def _status(mean: float) -> str:
        if mean >= 4.2:  return "✓ GOOD"
        if mean >= 3.5:  return "~ OK"
        return "✗ BELOW"

    lines = [
        f"# Synthetic Eval — Judge Summary",
        f"",
        f"**Run:** {timestamp}  ",
        f"**Questions:** {summary['n']}  ",
        f"**Mean judge score:** {summary['mean_score']} / 5.0  {_status(summary['mean_score'])}",
        f"**% scoring ≥ 4:** {summary['pct_gte_4']}%  (target ≥ 60%)",
        f"**% scoring ≤ 2:** {summary['pct_lte_2']}%  (target ≤ 15%)",
        f"",
        f"## Score distribution",
        f"",
        f"| Score | Count |",
        f"|---|---|",
    ]
    for s in range(5, 0, -1):
        lines.append(f"| {s} | {summary['score_dist'].get(str(s), 0)} |")

    lines += ["", "## By sector", "", "| Sector | Mean | n | Status |", "|---|---|---|---|"]
    for sector, v in summary["by_sector"].items():
        lines.append(f"| {sector} | {v['mean']} | {v['n']} | {_status(v['mean'])} |")

    lines += ["", "## By question type", "", "| Type | Mean | n | Status |", "|---|---|---|---|"]
    for qtype, v in summary["by_question_type"].items():
        lines.append(f"| {qtype} | {v['mean']} | {v['n']} | {_status(v['mean'])} |")

    lines += ["", "## By filing year", "", "| Year | Mean | n | Status |", "|---|---|---|---|"]
    for year, v in summary["by_year"].items():
        lines.append(f"| {year} | {v['mean']} | {v['n']} | {_status(v['mean'])} |")

    # Worst-performing questions
    low = sorted([r for r in results if r["score"] <= 2], key=lambda r: r["score"])[:10]
    if low:
        lines += ["", "## Lowest-scoring questions", ""]
        for r in low:
            lines.append(f"**[{r['score']}/5]** {r['id']}")
            lines.append(f"> {r['question']}")
            lines.append(f"*Missing:* {r.get('missing', '—')}")
            lines.append("")

    out = RESULTS_DIR / "summary.md"
    out.write_text("\n".join(lines), encoding="utf-8")
    return out


# ── Main ──────────────────────────────────────────────────────────────────────

def run(
    input_path: Path | None  = None,
    output_path: Path | None = None,
    judge_model: str = "gpt-5.4-mini",
    workers: int     = 6,
    limit: int | None = None,
) -> Path:
    from src.answer.answer import LLMClient, AnswerConfig
    from datetime import datetime

    input_path  = input_path  or (DATA_DIR / "rag_outputs.jsonl")
    output_path = output_path or (DATA_DIR / "judge_results.jsonl")
    DATA_DIR.mkdir(parents=True, exist_ok=True)

    print(f"\n── Stage 3: Judge LLM ────────────────────────────────────────────")
    print(f"  Input   : {input_path}")
    print(f"  Model   : {judge_model}  |  workers={workers}")

    if not input_path.exists():
        sys.exit(f"Input not found: {input_path} — run run_rag.py first")

    items: list[dict] = []
    with open(input_path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                items.append(json.loads(line))

    if limit:
        items = items[:limit]
    n = len(items)
    print(f"  Loaded {n} items\n")

    cfg = AnswerConfig(model=judge_model)
    llm = LLMClient(cfg)

    completed = 0
    results: dict[int, dict] = {}

    with ThreadPoolExecutor(max_workers=workers) as pool:
        futures = {
            pool.submit(_judge_one, (i, item, llm, 2)): i
            for i, item in enumerate(items)
        }
        for future in as_completed(futures):
            idx, r = future.result()
            results[idx] = r
            completed += 1
            print(
                f"  [{completed:3d}/{n}] {r['id']}  score={r['score']}  "
                f"{r.get('reasoning','')[:60]}",
                flush=True,
            )

    ordered = [results[i] for i in range(n)]

    # Write full results
    with open(output_path, "w", encoding="utf-8") as f:
        for r in ordered:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"\n  ✓ {n} judged results → {output_path}")

    # Build and write summary
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    summary   = build_summary(ordered)

    ts_path = RESULTS_DIR / f"{timestamp}.json"
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    ts_path.write_text(
        json.dumps({"timestamp": timestamp, "summary": summary, "results": ordered},
                   indent=2, ensure_ascii=False),
        encoding="utf-8",
    )

    md_path = write_summary_md(summary, ordered, timestamp)
    print(f"  Results → {ts_path}")
    print(f"  Summary → {md_path}\n")
    print(md_path.read_text(encoding="utf-8"))

    return output_path


def main() -> None:
    parser = argparse.ArgumentParser(description="Stage 3: judge RAG answers")
    parser.add_argument("--input",       type=Path, default=None,
                        help="Input JSONL (default: evals/data/rag_outputs.jsonl)")
    parser.add_argument("--output",      type=Path, default=None,
                        help="Output JSONL (default: evals/data/judge_results.jsonl)")
    parser.add_argument("--judge-model", default="gpt-5.4-mini",
                        help="LLM model for judging (default: gpt-5.4-mini)")
    parser.add_argument("--workers",     type=int, default=6)
    parser.add_argument("--limit",       type=int, default=None)
    args = parser.parse_args()

    run(
        input_path=args.input,
        output_path=args.output,
        judge_model=args.judge_model,
        workers=args.workers,
        limit=args.limit,
    )


if __name__ == "__main__":
    main()
